import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import yaml from "js-yaml";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const frontendRoot = resolve(scriptDir, "..");
const defaultSwaggerPath = resolve(frontendRoot, "../../Backend/YDuckBack/docs/swagger.yaml");
const swaggerPath = process.env.OPENAPI_PATH
  ? resolve(frontendRoot, process.env.OPENAPI_PATH)
  : defaultSwaggerPath;
const outputPath = resolve(frontendRoot, "app/lib/generated-api.ts");

const document = yaml.load(readFileSync(swaggerPath, "utf8"));
const schemas = document?.components?.schemas;

if (!schemas || typeof schemas !== "object") {
  throw new Error(`No OpenAPI component schemas found in ${swaggerPath}`);
}

const lines = [
  "// This file is generated by `npm run generate:api`.",
  "// Do not edit by hand.",
  "",
  "export type ApiSchemas = {",
];

for (const [name, schema] of Object.entries(schemas)) {
  lines.push(`  ${JSON.stringify(name)}: ${schemaToType(schema, { indent: 2 })};`);
}

lines.push("};", "");

writeFileSync(outputPath, `${lines.join("\n")}\n`);
console.log(`Generated ${outputPath} from ${swaggerPath}`);

function schemaToType(schema, options = {}) {
  const indent = options.indent ?? 0;
  if (!schema || typeof schema !== "object") {
    return "unknown";
  }

  if (schema.$ref) {
    return refToType(schema.$ref);
  }

  if (schema.enum) {
    return schema.enum.map((value) => JSON.stringify(value)).join(" | ");
  }

  let type;
  switch (schema.type) {
    case "object":
      type = objectToType(schema, indent);
      break;
    case "array":
      type = `${schemaToType(schema.items, { indent })}[]`;
      break;
    case "integer":
    case "number":
      type = "number";
      break;
    case "boolean":
      type = "boolean";
      break;
    case "string":
      type = "string";
      break;
    default:
      type = "unknown";
  }

  return schema.nullable ? `${type} | null` : type;
}

function objectToType(schema, indent) {
  const properties = schema.properties || {};
  const required = new Set(schema.required || []);
  const propertyEntries = Object.entries(properties);

  if (propertyEntries.length === 0) {
    return "Record<string, unknown>";
  }

  const basePadding = " ".repeat(indent);
  const childPadding = " ".repeat(indent + 2);
  const propertyLines = propertyEntries.map(([name, propertySchema]) => {
    const optional = required.has(name) ? "" : "?";
    return `${childPadding}${JSON.stringify(name)}${optional}: ${schemaToType(propertySchema, {
      indent: indent + 2,
    })};`;
  });

  return `{\n${propertyLines.join("\n")}\n${basePadding}}`;
}

function refToType(ref) {
  const prefix = "#/components/schemas/";
  if (!ref.startsWith(prefix)) {
    return "unknown";
  }
  return `ApiSchemas[${JSON.stringify(ref.slice(prefix.length))}]`;
}
